import * as z from "zod"
import { Bezahlmethoden, Rechnungsstatus, Service } from "@prisma/client"

export const RechnungSchema = z.object({
  id: z.number().int(),
  uid: z.string(),
  benutzer_id: z.number().int(),
  empfaenger: z.string().nullable(),
  strasse: z.string().nullable(),
  plz: z.string().nullable(),
  ort: z.string().nullable(),
  zusatzzeile: z.string().nullable(),
  telefon: z.string().nullable(),
  email: z.string().nullable(),
  abweichende_versand_adresse: z.boolean().nullable(),
  versand_empfaenger: z.string().nullable(),
  versand_strasse: z.string().nullable(),
  versand_plz: z.string().nullable(),
  versand_ort: z.string().nullable(),
  versand_zusatzzeile: z.string().nullable(),
  bezahlmethode: z.nativeEnum(Bezahlmethoden),
  status: z.nativeEnum(Rechnungsstatus),
  services: z.nativeEnum(Service).array(),
  betrag: z.number(),
  erstellt_am: z.date(),
  bezahlt_am: z.date().nullable(),
  storniert_am: z.date().nullable(),
  transaktions_referenz: z.string().nullable(),
  aufnahme_id: z.number().int(),
})
